OUT 43H

MVI A,00H

LOOP1:
	OUT 40H
	OUT 41H
	INR A
	CPI 0FFH
	JNZ LOOP1
LOOP2:
	OUT 40H
	OUT 41H
	DCR A
	JNZ LOOP2
	JMP LOOP1

DELAY:	MVI B,01H
OUTLOOP:LXI D, 0FFFFH
INLOOP:	DCX D
		MOV A,D
		ORA E
		JNZ INLOOP
		DCR B
		JNZ OUTLOOP
		RET

